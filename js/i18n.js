  function current() {
    const l = localStorage.getItem(KEY);
    return SUPPORTED.includes(l) ? l : 'en';
  }
  function t(key, lang) {
    lang = lang || current();
    if (T[lang] && T[lang][key] != null) return T[lang][key];
    return (T.en[key] != null) ? T.en[key] : key;
  }
  function apply(lang) {
    if (!SUPPORTED.includes(lang)) lang = 'en';
    localStorage.setItem(KEY, lang);
    document.documentElement.setAttribute('lang', lang);
    document.querySelectorAll('[data-i18n]').forEach(el => { const v = t(el.getAttribute('data-i18n'), lang); if (v != null) el.innerHTML = v; });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => { const v = t(el.getAttribute('data-i18n-ph'), lang); if (v != null) el.setAttribute('placeholder', v); });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => { const v = t(el.getAttribute('data-i18n-aria'), lang); if (v != null) el.setAttribute('aria-label', v); });
    document.querySelectorAll('[data-i18n-label]').forEach(el => { const v = t(el.getAttribute('data-i18n-label'), lang); if (v != null) el.setAttribute('label', v); });
    document.querySelectorAll('.lng').forEach(b => { const on = b.dataset.lang === lang; b.classList.toggle('active', on); b.setAttribute('aria-pressed', on ? 'true' : 'false'); });
    window.dispatchEvent(new CustomEvent('ocaq:lang', { detail: { lang } }));
  }

  window.OCAQ_I18N = { t, apply, current };

  function init() {
    apply(current());
    document.querySelectorAll('.lng').forEach(b => b.addEventListener('click', () => apply(b.dataset.lang)));
  }
  if (document.readyState !== 'loading') init();
  else document.addEventListener('DOMContentLoaded', init);
